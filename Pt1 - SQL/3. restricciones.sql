/*
	Restricciones de los Campos

	Unique
		Permite determinar que los valores registrados
		en una misma columna no sean idénticos, es decir se mantengan únicos.
		Por ejemplo el email de una persona es un campo único.

	Check
		Permite restringir el rango de valores que pueden estar permitidos
		ingresar en una o más columnas de una tabla.

	Default
		Permite registrar un dato en un campo por defecto cuando el usuario
		no ingresa ningún valor, la propiedad del campo de la tabla
		necesariamente tiene que ser Null.
		Ejemplo se puede Utilizar para los campos de tipo Date(FECHA), cuando
		el usuario no ingresa ninguna fecha que se asigne la fecha actual.
*/