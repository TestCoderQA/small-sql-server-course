/*
	BackUP. Es una copia de seguridad, copia de respaldo en tecnologías
	de la información es una copia de los datos originales que se realiza
	con el fin de disponer de un medio para recuperarlos en caso de su pérdida.

	Los BackUp son útiles ante distintos eventos y usos:

	- Recuperar los datos de los sistemas informaticos de 
	una catástrofe informatica, natural o ataque.

	- Restaurar una pequeña cantidad de información que puede
	haberse eliminado accidentalmente, corrompido.

	Restore, El proceso de copia de seguridad se completa con otro
	conocido como restauración de los daos (en inglés restore), que
	es la acción de leer y grabar en la ubicación original u otra alternativa
	los datos requeridos que han sido respaldados con anterioridad.

	-------------************************************-----------------------
							Back UP
	Pasos:

	1. Crear una carpeta en el disco local C://
	2. Abrir un bloc de notas.
	3. Guardarlo con la extensión .bak (dbreservabackup.bak)
	4. Click derecho sobre la base de datos en sql server
	5. Tareas(taks) -> Copia de seguridad(Back up...)
	6. En el asistente eligir tipo FULL.
	7. Remover el directorio donde se va a guardar.
	8. Escoger el archivo. bak que acabamos de crear.

	-------------************************************-----------------------
							Restore
	Pasos:

	1. No tener la base de datos en el motor de bases de datos.
	2. Click derecho sobre Bases de datos(Database).
	3. Click sobre restaurar bases de datos(Restore Database).
	4. Eligir la opción, Dispositivo buscar el archivo .bak
	5. Aceptar -> Aceptar.						
*/
