/*
	El lengujae SQL está compuesto por comandos,
	cláusulas, operadores y funciones de agregados.
	Estos elementos se Combinan en las instrucciones para
	crear, actualizar y Manipular las base de datos.

	Existen 2 tipos de Comandos SQL:

		Lenguaje de Definición de Datos(DDL)
		comandos para crear y definir nuevas base de datos, tablas,
		campos e índices.

		Lengujae de Manipulación de Datos(DML)
		Comandos para insertar, modificar y eliminar
		registros, así como ordenar, filtrar y extraer
		información de la base de datos.

		-Insert
		-Update
		-Delete
		-Select
		-Merge
		-Bulk Insert

	Lenguaje de Definición de Datos (LDD)

	SENTENCIA CREATE (Comando de Creación de Objetos de la Base de Datos):
	Permite crear base de datos, tablas, desencadenadores, procedimientos,
	funciones, vistas e Índices de una base de Datos.

	SENTENCIA ALTER(Comando de Modificación de Objetos de la base de Datos):
	Permite la modificación de un objeto asociado a una base de Datos, puede
	modificar archivos, grupo de archivos, cambiar atributos de un Objeto.

	SENTENCIA DROP(Comando de Eliminacion de Objetos de la Base de Datos):
	Permite la eliminación de un objeto asociado a una Base de Datos.
*/